// Напишите функцию calculateAverage, которая принимает 2 числа и возвращает их среднее арифметическое


function calculateAverage(number_1, number_2) {// функция принимает два числа в качестве аргументов".
  return (number_1 + number_2) / 2 // возвращаем среднее арифметическое двух чисел спомощью return.
}
let result = calculateAverage(10, 20) //вызываем функцию с числами 10 и 20,
//результат сохраняем в переменной result.
console.log(result)// выводим в консольт результат
